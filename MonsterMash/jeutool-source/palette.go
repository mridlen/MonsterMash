var pushed_palette [256]color.NRGBA

// PalettePush saves the current palette, which can later be
// restored via PalettePop.  Only a single save is possible
// at any one time (i.e. there is no stack).
func PalettePush() {
	pushed_palette = palette
}

// PalettePop restores the palette which was previously
// pushed.  The hash map is also recomputed.
func PalettePop() {
	palette = pushed_palette

	PaletteCreateHash()
}

