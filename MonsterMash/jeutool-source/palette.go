var palette [256]color.NRGBA

// this is only for the "Loaded xxx" message
var palette_from string

var pal_hash map[int]int

// PaletteCreateHash creates the hash-map, allowing colors
// which occur in the palette to be found quickly.
func PaletteCreateHash() {
	pal_hash = make(map[int]int)

	for c := 0; c < 256; c++ {
		key := 0
		key |= int(palette[c].R) << 16
		key |= int(palette[c].G) << 8
		key |= int(palette[c].B)

		// skip if already there
		_, ok := pal_hash[key]
		if ok {
			continue
		}

		// ensure the value is never zero
		// [ to distinguish it from a missing key ]
		pal_hash[key] = 1024 | c
	}
}

// PaletteLookup finds the color in the palette closest to
// the given one, and returns its index.  Alpha values less
// than the maximum cause the input color to be blended with
// black.  The alpha values in palette[] are ignored.
func PaletteLookup(c color.Color) (best byte) {
	// convert color to RGB with 8 bits per channel
	r1, g1, b1, _ := c.RGBA()

	r2 := int(r1) >> 8
	g2 := int(g1) >> 8
	b2 := int(b1) >> 8

	// try the hash-map first
	key := (r2 << 16) | (g2 << 8) | b2
	hashed := pal_hash[key]
	if hashed != 0 {
		return byte(hashed & 0xff)
	}

	// use the much slower method
	best_dist := (1 << 30)

	for c := 0; c < 256; c++ {
		dr := r2 - int(palette[c].R)
		dg := g2 - int(palette[c].G)
		db := b2 - int(palette[c].B)

		dist := dr*dr + dg*dg + db*db

		if dist < best_dist {
			best = byte(c)
			best_dist = dist
		}
	}

	return
}

