// FullPalette represents the PLAYPAL lump, which generally
// consists of a set of 256-color palettes.  It can be used
// for single palettes too (e.g. Heretic's E2PAL lump).
// It should be a multiple of 768 bytes.
type FullPalette []byte

func PLAYPAL_LoadRaw(filename string) (FullPalette, error) {
	data, err := ioutil.ReadFile(filename)
	if err != nil {
		return nil, err
	}

	if len(data) < 768 {
		return nil, fmt.Errorf("bad palette file (too short)")
	}

	// truncate when not a multiple of 768
	if len(data)%768 != 0 {
		new_size := (len(data) / 768) * 768
		data = data[0:new_size]
	}

	palette_from = filepath.Base(filename)
	return data, Ok
}

func PLAYPAL_LoadImage(filename string) (FullPalette, error) {
	f, err := os.Open(filename)
	if err != nil {
		return nil, err
	}

	defer f.Close()

	img, err := png.Decode(f)
	if err != nil {
		return nil, err
	}

	W := img.Bounds().Dx()
	H := img.Bounds().Dy()

	LX := img.Bounds().Min.X
	LY := img.Bounds().Min.Y

	// we support two particular layouts:
	//   (a) 16x16, just one palette, row major order
	//   (b) 256xN, where each row is a single palette

	if W == 16 && H == 16 {
		// ok
	} else if W == 256 && H > 0 {
		// ok
	} else {
		return nil, fmt.Errorf("unsupported image size: %dx%d", W, H)
	}

	total := W * H

	res := make(FullPalette, total*3)

	for i := 0; i < total; i++ {
		x := i % W
		y := i / W

		r, g, b, _ := img.At(LX+x, LY+y).RGBA()

		res[i*3+0] = byte(r >> 8)
		res[i*3+1] = byte(g >> 8)
		res[i*3+2] = byte(b >> 8)
	}

	palette_from = filepath.Base(filename)
	return res, Ok
}

func PLAYPAL_SaveRaw(filename string, pal FullPalette) error {
	if len(pal) < 768 {
		return fmt.Errorf("bad palette data (too short)")
	}
	return ioutil.WriteFile(filename, pal, 0644)
}

func PLAYPAL_SaveImage(filename string, pal FullPalette) error {
	if len(pal) < 768 {
		return fmt.Errorf("bad palette data (too short)")
	}

	f, err := os.Create(filename)
	if err != nil {
		return err
	}

	defer SafeClose(f, filename)

	W := 16
	H := 16

	if len(pal) > 768 {
		W = 256
		H = len(pal) / 768
	}

	total := W * H

	img := image.NewNRGBA(image.Rect(0, 0, W, H))

	for i := 0; i < total; i++ {
		x := i % W
		y := i / W

		r := pal[i*3+0]
		g := pal[i*3+1]
		b := pal[i*3+2]

		img.SetNRGBA(x, y, color.NRGBA{r, g, b, 255})
	}

	return png.Encode(f, img)
}

