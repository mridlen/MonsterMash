func PaletteLoad(filename string) error {
	// check for built-in names
	switch strings.ToLower(filename) {
	case "doom":
		return PaletteSetDoom()
	case "heretic":
		return PaletteSetHeretic()
	case "hexen":
		return PaletteSetHexen()
	case "strife":
		return PaletteSetStrife()
	}

	if HasExtension(filename, "txt") {
		return PaletteLoadText(filename)
	}

	if HasExtension(filename, "wad") {
		return PaletteLoadFromWad(filename, "PLAYPAL")
	}

	if HasExtension(filename, "png") {
		pal, err := PLAYPAL_LoadImage(filename)
		if err != nil {
			return err
		}
		return PaletteLoadBuffer(pal)
	}

	if HasExtension(filename, "raw") ||
		HasExtension(filename, "dat") ||
		HasExtension(filename, "lmp") {

		pal, err := PLAYPAL_LoadRaw(filename)
		if err != nil {
			return err
		}
		return PaletteLoadBuffer(pal)
	}

	return fmt.Errorf("unsupported file format")
}

func PaletteLoadText(filename string) error {
	data, err := TXT_Load(filename)
	if err != nil {
		return err
	}

	bb := bytes.NewReader(data)

	for c := 0; c < 256; c++ {
		var r, g, b byte

		_, err := fmt.Fscan(bb, &r, &g, &b)
		if err != nil {
			return err
		}

		palette[c] = color.NRGBA{r, g, b, 255}
	}

	palette_from = filepath.Base(filename)
	PaletteCreateHash()
	return Ok
}

func PaletteSaveText(filename string) error {
	var bb bytes.Buffer

	for c := 0; c < 256; c++ {
		r := palette[c].R
		g := palette[c].G
		b := palette[c].B

		fmt.Fprintf(&bb, "%3d %3d %3d\n", r, g, b)
	}

	return TXT_Save(filename, bb.Bytes())
}

func PaletteLoadBuffer(buf []byte) error {
	if len(buf) < 256*3 {
		return fmt.Errorf("bad palette data (too short)")
	}

	for c := 0; c < 256; c++ {
		palette[c].R = buf[c*3+0]
		palette[c].G = buf[c*3+1]
		palette[c].B = buf[c*3+2]
		palette[c].A = 255
	}

	PaletteCreateHash()
	return Ok
}

func PaletteLoadFromWad(filename, lumpname string) error {
	w, err := wad.Open(filename)
	if err != nil {
		return err
	}

	defer w.Close()

	lump := w.FindLump(lumpname)
	if lump == nil {
		return fmt.Errorf("no %s in wad", lumpname)
	}

	data, err := w.ReadLump(lump)
	if err != nil {
		return err
	}

	err = PaletteLoadBuffer(data)
	if err != nil {
		return err
	}

	palette_from = filepath.Base(filename)
	PaletteCreateHash()
	return Ok
}

func PaletteLoadBuiltin(colors ...int) {
	// each color is hexadecimal notation: 0xRRGGBB

	if len(colors) != 256 {
		panic("bad size for builtin palette")
	}

	for c, rgb := range colors {
		palette[c].R = byte(rgb >> 16)
		palette[c].G = byte(rgb >> 8)
		palette[c].B = byte(rgb)
		palette[c].A = 255
	}

	PaletteCreateHash()
}

